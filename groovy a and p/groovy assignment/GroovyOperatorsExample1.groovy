class GroovyOperatorsExample1 {  
static void main(args) {  
        int a = 10  
        int b = 5  
        int c  
        c = a + b  
        println "Addition = " + c  
        c = a - b  
        println "Subtraction = " + c  
        c = a * b  
        println "Multiplication = " + c  
        c = a / b  
        println "Division = " + c  
        c = a % b  
        println "Remainder  = " + c  
        c = a ** b  
        println "Power = "+c  
        }  
}  